<h1>Aviso de Privacidad</h1>
<p class="date">Fecha de vigencia: 19 de marzo de 2026</p>

<p>CotizaCloud ("nosotros", "la plataforma") se compromete a proteger la privacidad de sus usuarios. Este aviso describe qué datos recopilamos, cómo los usamos y los derechos que tienes sobre tu información.</p>

<h2>1. Datos que recopilamos</h2>
<ul>
    <li><strong>Datos de cuenta:</strong> nombre, correo electrónico y contraseña al registrarte.</li>
    <li><strong>Datos de tu empresa:</strong> nombre de empresa, logo, configuraciones fiscales y artículos/servicios que agregas.</li>
    <li><strong>Datos de clientes:</strong> nombres, correos, teléfonos y direcciones de los clientes que registras en la plataforma.</li>
    <li><strong>Cotizaciones y ventas:</strong> los documentos que creas dentro de la plataforma.</li>
    <li><strong>Datos de uso:</strong> interacciones con la plataforma, direcciones IP y datos del dispositivo para mejorar el servicio.</li>
    <li><strong>Notificaciones push:</strong> tokens de dispositivo para enviar notificaciones a la app móvil.</li>
</ul>

<h2>2. Uso de la información</h2>
<p>Utilizamos tu información para:</p>
<ul>
    <li>Proveer y mantener el servicio de cotizaciones y ventas.</li>
    <li>Enviar notificaciones relevantes sobre tu actividad (cotizaciones aceptadas, actividad del radar, tickets de soporte).</li>
    <li>Mejorar la plataforma y la experiencia del usuario.</li>
    <li>Comunicarnos contigo sobre tu cuenta o cambios en el servicio.</li>
</ul>

<h2>3. Compartición de datos</h2>
<p>No vendemos, alquilamos ni compartimos tu información personal con terceros, excepto:</p>
<ul>
    <li>Cuando es necesario para proveer el servicio (por ejemplo, enviar notificaciones push a través de Apple Push Notification Service).</li>
    <li>Cuando la ley lo requiera.</li>
</ul>

<h2>4. Seguridad</h2>
<p>Protegemos tu información con medidas de seguridad estándar de la industria, incluyendo cifrado de contraseñas, conexiones HTTPS y acceso restringido a los datos.</p>

<h2>5. Retención de datos</h2>
<p>Conservamos tu información mientras tu cuenta esté activa. Si eliminas tu cuenta, tus datos serán eliminados de nuestros servidores en un plazo de 30 días.</p>

<h2>6. Tus derechos</h2>
<p>Tienes derecho a:</p>
<ul>
    <li>Acceder a tus datos personales.</li>
    <li>Solicitar la corrección de datos inexactos.</li>
    <li>Solicitar la eliminación de tu cuenta y datos.</li>
    <li>Revocar tu consentimiento en cualquier momento.</li>
</ul>

<h2>7. Cookies</h2>
<p>Utilizamos cookies de sesión necesarias para el funcionamiento de la plataforma.</p>

<h2>8. Tecnologías de seguimiento de terceros</h2>
<p>Algunas empresas que utilizan CotizaCloud pueden incluir tecnologías de seguimiento (pixels de seguimiento) de plataformas publicitarias como Meta (Facebook/Instagram), Google Analytics, Google Ads y TikTok en las cotizaciones y ventas que te envían.</p>
<p>Estas tecnologías permiten a la empresa que te envió la cotización medir la efectividad de sus campañas publicitarias. La información recopilada por estos pixels está sujeta a las políticas de privacidad de cada plataforma:</p>
<ul>
    <li><strong>Meta:</strong> facebook.com/privacy/policy</li>
    <li><strong>Google:</strong> policies.google.com/privacy</li>
    <li><strong>TikTok:</strong> tiktok.com/legal/privacy-policy</li>
</ul>
<p>Si deseas más información sobre el uso de estas tecnologías, contacta directamente a la empresa que te envió la cotización.</p>

<h2>9. Cambios a este aviso</h2>
<p>Podemos actualizar este aviso de privacidad ocasionalmente. Publicaremos cualquier cambio en esta página con la fecha de actualización.</p>

<h2>10. Contacto</h2>
<p>Si tienes preguntas sobre este aviso de privacidad, contáctanos a través de la sección de <a href="/ayuda">Ayuda y Soporte</a> dentro de la plataforma.</p>

