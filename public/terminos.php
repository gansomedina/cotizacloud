<h2>4. Descripción del servicio</h2>

<p>CotizaCloud es una <strong>plataforma tecnológica</strong> que permite a las Empresas crear cotizaciones profesionales, enviarlas a sus Clientes Finales mediante un enlace, dar seguimiento a la interacción del cliente, convertir cotizaciones en ventas, registrar pagos y emitir recibos.</p>

<div class="highlight">
    <strong>Importante:</strong> CotizaCloud es exclusivamente una herramienta tecnológica. No es vendedor, distribuidor, intermediario, corredor, asesor ni garante de ningún producto o servicio que las Empresas ofrezcan a través de la Plataforma. CotizaCloud no participa en la relación comercial entre la Empresa y sus Clientes Finales.
</div>

<p>Las funcionalidades específicas pueden variar según el plan contratado y pueden ser modificadas, ampliadas o descontinuadas por CotizaCloud en cualquier momento, con aviso previo razonable cuando se trate de funcionalidades esenciales.</p>

