<p>En caso de que el cargo recurrente no pueda procesarse, CotizaCloud otorgará un período de gracia de <strong>7 días naturales</strong>. Durante este período, el servicio continuará activo y se enviarán avisos por correo electrónico. Si al concluir el período de gracia el pago no se regulariza, la cuenta será degradada al plan Free.</p>

<h3>5.4. Cancelación</h3>
<p>El Usuario puede cancelar su suscripción en cualquier momento desde la sección de Configuración → Suscripción de su cuenta, <strong>sin penalización alguna</strong>. La cancelación surtirá efecto al término del período ya pagado, conservando el acceso hasta esa fecha.</p>

<h3>5.5. Reembolsos</h3>
<p>Las cuotas pagadas <strong>no son reembolsables</strong>, salvo en caso de cobros duplicados o errores comprobables de facturación, los cuales serán reembolsados dentro de los 15 días hábiles siguientes a la notificación del error.</p>

